    private void btnLlenaMatrizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLlenaMatrizActionPerformed
        abs=new Imprime();
        
        if (matAnt.length<=0) {
            n1=Integer.parseInt(txtAlto.getText());
            m1=Integer.parseInt(txtAncho.getText());
            matAnt=abs.llena(n1,m1);
            txtMostrar.setText(imprimir(matAnt, n1, m1));
            String si= JOptionPane.showInputDialog("Si desea sumar o restar, ingrese los datos de la otra matriz \n digite SI, si lo desea \nDe lo contratio digite NO");
            if (si.equalsIgnoreCase("si")) {
                txtAlto.setText("");
                txtAncho.setText("");
            }  
        }else{
            n2=Integer.parseInt(txtAlto.getText());
            m2=Integer.parseInt(txtAncho.getText());
            matNuv=abs.llena(n2, m2);
        }
        
    }//GEN-LAST:event_btnLlenaMatrizActionPerformed

