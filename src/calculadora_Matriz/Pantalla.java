    private void btnSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaActionPerformed
        abs=new Suma();
        if (n1==n2 && m2==m1) {
            matAnt=abs.suma(matAnt, matNuv, n1, m1);
            txtResultado.setText(imprimir(matAnt, n1, m1));
        }else{
            JOptionPane.showMessageDialog(null, "Las matrices no son compatibles para una suma");
            fallo();
        }
    }//GEN-LAST:event_btnSumaActionPerformed

    private void btnRestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestaActionPerformed
        abs=new Resta();
        if (n1==n2 && m2==m1) {
            matAnt=abs.resta(matAnt, matNuv, n1, m1);
            txtResultado.setText(imprimir(matAnt, n1, m1));
        }else{
            JOptionPane.showMessageDialog(null, "Las matrices no son compatibles para una suma");
            fallo();
        }
    }//GEN-LAST:event_btnRestaActionPerformed

    private void btnTrazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrazaActionPerformed
        abs=new Traza();
        String res="";
        if (n1==m1) {
            result=abs.traza(matAnt, n1, m1);
            res=result+"";
            txtResultado.setText(res);
        }else{
            JOptionPane.showMessageDialog(null, "Las matrices no son compatibles para una suma");
            fallo();
        }
    }//GEN-LAST:event_btnTrazaActionPerformed

    private void btnDeterminanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeterminanteActionPerformed
        abs=new Determinante();
        String res="";
        if (n1==m1) {
            result=abs.determinante(matAnt, n1);
            res=result+"";
            txtResultado.setText(res);
        }else{
            JOptionPane.showMessageDialog(null, "Las matrices no son compatibles para una suma");
            fallo();
        }
    }//GEN-LAST:event_btnDeterminanteActionPerformed

