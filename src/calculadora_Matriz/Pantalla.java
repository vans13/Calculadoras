    public String imprimir(int[][] a, int b, int c){
        String s="";
        for (int i = 0; i < b; i++) {
            for (int j = 0; j < c; j++) {
                s+="["+a[i][j]+"] ";
            }
            s+="\n";
        }
        
        return s;
    }
    

